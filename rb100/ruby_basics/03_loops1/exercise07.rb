# Count Up
# The following code outputs a countdown from 10 to 1. Modify the code
# so that it counts from 1 to 10 instead.
#
# count = 10
#
# until count == 0
#   puts count
#   count -= 1
# end

count = 1

until count > 10
  puts count
  count += 1
end

# Notes
# The `until` loop is the opposite of the `while` loop.
# `while` iterates until the condition evaluates as FALSE.
# `until` iterates until the condition evaluates as TRUE.

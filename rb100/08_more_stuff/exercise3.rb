# What is exception handling and what problem does it solve?

# It is a way to account for unhandled errors that sometimes we're not
# able to see it coming. It solves the problem of making the error
# meaningful instead of the compiler/interpreter spits out gibberish and
# daunting error messages.

# LS Answer
# Exception handling is a structure used to handle the possibility of
# an error occurring in a program. It is a way of handling the error by
